// File: Pelanggan.java
public class Pelanggan {
    private String nama;
    private String nomor;

    // Constructor
    public Pelanggan(String nama, String nomor) {
        this.nama = nama;
        this.nomor = nomor;
    }

    // Getter methods
    public String getNama() {
        return nama;
    }

    public String getNomor() {
        return nomor;
    }
}
